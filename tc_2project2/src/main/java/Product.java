public class Product {
	private String id;
 
	private String productname;
    private String productdescription;
    private String functionality;
    private String performance;
    private String usability;
    private String cost;
    private String value;
    private String environmental_impact;
    private String customer_feedback;
    
    public String getId() {
 		return id;
 	}
 	public void setId(String id) {
 		this.id = id;
 	}
    
	public String getProductname() {
		return productname;
	}
	public void setProductname(String productname) {
		this.productname = productname;
	}
	public String getProductdescription() {
		return productdescription;
	}
	public void setProductdescription(String productdescription) {
		this.productdescription = productdescription;
	}
	public String getFunctionality() {
		return functionality;
	}
	public void setFunctionality(String functionality) {
		this.functionality = functionality;
	}
	public String getPerformance() {
		return performance;
	}
	public void setPerformance(String performance) {
		this.performance = performance;
	}
	public String getUsability() {
		return usability;
	}
	public void setUsability(String usability) {
		this.usability = usability;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getEnvironmental_impact() {
		return environmental_impact;
	}
	public void setEnvironmental_impact(String environmental_impact) {
		this.environmental_impact = environmental_impact;
	}
	public String getCustomer_feedback() {
		return customer_feedback;
	}
	public void setCustomer_feedback(String customer_feedback) {
		this.customer_feedback = customer_feedback;
	}
    
}